namespace 变量
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // 整数类型
            int IntNumber = 100;
            Console.WriteLine(IntNumber);

            // 浮点值类型
            double DoubleNumber = 1.1;
            Console.WriteLine(DoubleNumber);

            // 资金类型
            decimal DecimalNumber = 100m;
            Console.WriteLine(DecimalNumber);

            // 字符串类型
            string StringName = "Xiaoming Wang";
            Console.WriteLine(StringName);

            // 字符类型（单个字符）
            char CharName = 'L';
            Console.WriteLine(CharName);
        }
    }
}
